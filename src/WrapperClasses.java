
public class WrapperClasses {

	public static void main(String[] args) {
		// wrapper classes = provides a way to use primitive data types as reference data types
		//                   reference data types contain useful methods
		//                   can be used with collections (ex. Arraylist)
		
	    // primitive     //wrapper
		//**********     //********
		//boolean        Boolean
		//char           Character
		//int            Integer
		//double         Double
		
		// autoboxing = the automatic conversion that the Java compiler makes between the primitive type
		// unboxing = the reverse of autoboxing. Automatic conversion os wrapper class to primitive
		Boolean a = true;
		Character b = '@';
		Integer c = 123;
		Double d = 3.14;
		String e = "Alex";
		
		a.compareTo(b);
		

	}

}
